#include "libft.h"

void	ft_putnbr_tab(int *tab)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (tab[j])
		j++;
	while (tab[i])
	{
		ft_putnbr(tab[i]);
		if (j - 1 > i)
			ft_putchar('\n');
		i++;
	}
}
